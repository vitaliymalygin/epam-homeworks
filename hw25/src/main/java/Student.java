public class Student {
    private String name;

    private Discipline discipline;

    public Student(String name, Discipline discipline) {
        this.name = name;
        this.discipline = discipline;
    }

    public String getName() {
        return name;
    }



    public Discipline getDiscipline() {
        return discipline;
    }





}
